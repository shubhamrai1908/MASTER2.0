package shubham.latest.master20;


public class Ground {
    public String name;
    public String phone;

    public Ground(String name, String phone) {
        this.name = name;
        this.phone = phone;
    }
    public Ground()
    {

    }


    public String getname() {
        return name;
    }

    public String getphone() {
        return phone;
    }
}
